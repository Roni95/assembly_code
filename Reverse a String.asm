CODE SEGMENT
 ASSUME DS:DATA, CS:CODE , ES:DATA
START:
 MOV AX,DATA
 MOV DS,AX
 MOV ES,AX
 MOV CX,COUNT
 MOV SI,0
 MOV DI,0
 ADD DI,COUNT
 DEC DI

COPY:
 MOV AL,TEXT1[SI]
 MOV TEXT2[DI],AL
 DEC DI
 INC SI
 LOOP COPY
